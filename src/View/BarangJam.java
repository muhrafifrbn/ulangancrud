        public void setColumnWidth(int kolom) {
        DefaultTableColumnModel dtcm = (DefaultTableColumnModel)  tabelBarang.getColumnModel();
        TableColumn kolomtabel = dtcm.getColumn(kolom);
        int lebar = 0;
        int margin = 10;
        int a;
        TableCellRenderer renderer = kolomtabel.getHeaderRenderer();
        if (renderer == null) {
            renderer = tabelBarang.getTableHeader().getDefaultRenderer();
        }
        Component komponen = renderer.getTableCellRendererComponent( tabelBarang, kolomtabel.getHeaderValue(), false, false, 0, 0);
        lebar = komponen.getPreferredSize().width;
        for (a = 1; a <  tabelBarang.getRowCount(); a++) {
            renderer =  tabelBarang.getCellRenderer(a, kolom);
            komponen = renderer.getTableCellRendererComponent( tabelBarang,  tabelBarang.getValueAt(a, kolom), false, false, a, kolom);
            int lebarKolom = komponen.getPreferredSize().width;
            lebar = Math.max(lebar, lebarKolom);
        }
        lebar = lebar + margin;
        kolomtabel.setPreferredWidth(lebar);
    }
    
    public void setLebarKolom() {
        int a;
        for (a = 0; a <  tabelBarang.getColumnCount(); a++) {
            setColumnWidth(a);
        }
    }
